// Loops 

// 1). For Loop :
let num=10;

for(let i=0;i<num;i++){
    console.log(i);
} 

// 2). Nested for Loop 

for(let i=0;i<5;i++){
    for(let j=0;j<5;j++){
        console.log(j);
    }
} 

// 3. While Loop :

let i=1;

while(i<6){
    console.log("hello");
    i++;
} 


